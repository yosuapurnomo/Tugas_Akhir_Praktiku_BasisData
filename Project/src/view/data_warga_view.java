    public void showComboBoxKK() throws SQLException{
        DefaultComboBoxModel dtmKK = new DefaultComboBoxModel();
        for(kartu_keluarga kk : this.controller.getKartu_Keluarga()){
            dtmKK.addElement(kk.getNo_kk());
        }
        this.cbKK.setModel(dtmKK);
    }
    public void showComboBoxStatus(){
        this.cbStatusKel.removeAllItems();
        this.cbStatusKel.addItem("Bapak");
        this.cbStatusKel.addItem("Ibu");
        this.cbStatusKel.addItem("Anak");
    }
    
    public void showComboBoxJenisKelamin(){
        this.cbJenisKelamin.removeAllItems();
        this.cbJenisKelamin.addItem("Pria");
        this.cbJenisKelamin.addItem("Wanita");
    }
    
    public void showTableKK() throws SQLException{
        DefaultTableModel dtmKK = new DefaultTableModel(new String[]{"NO KK", "KOTA", "ALAMAT", "JUMLAH ORANG"},0);
        dtmKK.setRowCount(0);
        for(jumlah_warga jw : this.controller.getJumlah_warga()){
            dtmKK.addRow(new String[]{jw.getNO_KK(), jw.getKota(), jw.getAlamat(), jw.getJumlah()});
        }
        this.tblKK.setModel(dtmKK);
    }
    
    public void showTableWarga() throws SQLException{
        DefaultTableModel dtmwarga = new DefaultTableModel(new String[]{"NO_KK", "NIK", "NAMA", "STATUS KELUARGA"},0);
        dtmwarga.setRowCount(0);
        for(data_warga dw : this.controller.getDataWarga()){
            dtmwarga.addRow(new String[]{dw.getNo_kk(), dw.getNik().toString(), dw.getNama(), dw.getStatus_keluarga()});
        }
        this.tblwarga.setModel(dtmwarga);
    }

