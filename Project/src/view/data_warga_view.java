    private void btnSetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetActionPerformed
        try {
            data_warga data = this.controller.GetDataWargaNik(this.tblwarga.getValueAt(this.tblwarga.getSelectedRow(), 1).toString());
            this.tfNik.setText(data.getNik().toString());
            Integer coun = 0;
            for(kartu_keluarga dw : this.controller.getKartu_Keluarga()){
                if(!this.tblwarga.getValueAt(this.tblwarga.getSelectedRow(), 0).toString().equals(dw.getNo_kk())){
                    coun+=1;
                }else{
                    break;
                }
            }
            
            this.cbKK.setSelectedIndex(coun);
            this.tfNama.setText(data.getNama());
            if(data.getStatus_keluarga().equals("Bapak")){
                coun = 0;
            }
            else if (data.getStatus_keluarga().equals("Ibu")){
                coun = 1;
            }
            else{
                coun = 2;
            }
            this.cbStatusKel.setSelectedIndex(coun);
            String tgl = new SimpleDateFormat("dd/MM/yyyy").format(data.getTanggal_lahir());
            this.tfTanggalLahir.setText(tgl);
            if(data.getJenis_kelamin().equals("Pria")){
                coun = 0;
            }
            else{
                coun = 1;
            }
            this.cbJenisKelamin.setSelectedIndex(coun);
            this.tfUmur.setText(data.getUmur().toString());
            this.tfNoTelp.setText(data.getNo_telp());
            this.nik = this.tblwarga.getValueAt(this.tblwarga.getSelectedRow(), 1).toString();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnSetActionPerformed

