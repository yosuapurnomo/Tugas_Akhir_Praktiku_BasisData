    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        try{
            String select = this.tblKK.getValueAt(this.tblKK.getSelectedRow(), 0).toString();
            kartu_keluarga kk = this.controller.getDetail_Keluarga(select);
            
            String text = ""
                + "NO KARTU KELUARGA : " + kk.getNo_kk() + "\n"
                    + "KODE POS : " + kk.getKode_pos() + "\n"
                    + "PROVINSI : " + kk.getProvinsi() + "\n"
                    + "KOTA : " + kk.getKota() + "\n"
                    + "ALAMAT : " + kk.getAlamat() + "\n"
                    + "\nJUMLAH ORANG : " + kk.getArrdata_warga().get(0).getJumlah();
            if(kk.getArrdata_warga().get(0).getJumlah().equals("0")){
                text += "\n------------------------------------------------------";
            }else{
                ArrayList<data_warga> dw = kk.getArrdata_warga();
            for(int i = 0;i < dw.size();++i){
            text += "\n"
                    + "NIK : " + dw.get(i).getNik().toString() + "\n"
                    + "NAMA : " + dw.get(i).getNama() + "\n"
                    + "STATUS KELUARGA : " + dw.get(i).getStatus_keluarga() + "\n"
                    + "TANGGAL LAHIR : " + new SimpleDateFormat("dd/MM/yyyy").format(dw.get(i).getTanggal_lahir()) + "\n"
                    + "JENIS KELAMIN : " + dw.get(i).getJenis_kelamin() + "\n"
                    + "UMUR : " + dw.get(i).getUmur().toString() + "\n"
                    + "NO TELP : " + dw.get(i).getNo_telp() + "\n";
            }
            }
            this.taDetail.setText(text);
            
        }catch (SQLException ex) {
                Logger.getLogger(data_warga_view.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
            try {
                data_warga data = new data_warga();
                data.setNik(Integer.parseInt(this.tfNik.getText()));
                data.setNo_kk(this.cbKK.getSelectedItem().toString());
                data.setNama(this.tfNama.getText());
                data.setStatus_keluarga(this.cbStatusKel.getSelectedItem().toString());
                data.setTanggal_lahir(new SimpleDateFormat("dd/MM/yyyy").parse(this.tfTanggalLahir.getText()));
                data.setJenis_kelamin(this.cbJenisKelamin.getSelectedItem().toString());
                data.setUmur(Integer.parseInt(this.tfUmur.getText()));
                data.setNo_telp(tfNoTelp.getText());
                
                this.controller.insert_warga(data);
                this.showTableKK();
                this.showTableWarga();
                tfNama.setText("");
                tfNoTelp.setText("");
                tfTanggalLahir.setText("");
                tfUmur.setText("");
            } catch (ParseException ex) {
                Logger.getLogger(data_warga_view.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(data_warga_view.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnTambahActionPerformed

